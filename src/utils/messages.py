    # =========================================================================
    # ФОРМАТИРОВАНИЕ
    # =========================================================================
    class Format:
        @staticmethod
        def array_display(arr, name="Массив", limit=20):
            """Форматирование отображения массива."""
            if not arr:
                return f"{name}: [] (пустой)"

            if len(arr) <= limit:
                return f"{name} [{len(arr)}]: {arr}"
            else:
                first = arr[:5]
                last = arr[-5:]
                return f"{name} [{len(arr)}]: {first} ... {last}"

        @staticmethod
        def matrix_display(matrix, name="Матрица"):
            """Форматирование отображения матрицы."""
            if not matrix:
                return f"{name}: [] (пустая)"

            result = f"{name} [{len(matrix)}x{len(matrix[0])}]:\n"
            for i, row in enumerate(matrix):
                result += f"  Строка {i + 1}: {row}\n"
            return result.strip()

        @staticmethod
        def success(message):
            """Форматирование успешного сообщения."""
            return f"✓ {message}"

        @staticmethod
        def error(message):
            """Форматирование сообщения об ошибке."""
            return f"✗ {message}"

        @staticmethod
        def warning(message):
            """Форматирование предупреждения."""
            return f"⚠ {message}"

        @staticmethod
        def info(message):
            """Форматирование информационного сообщения."""
            return f"ℹ {message}"

        @staticmethod
        def section(title):
            """Форматирование раздела."""
            return f"\n{'-' * 60}\n{title}\n{'-' * 60}"

        @staticmethod
        def subsection(title):
            """Форматирование подраздела."""
            return f"\n{'-' * 40}\n{title}\n{'-' * 40}"